class Gallery extends Component {
  state = {
    imgList: [...imagesList],
  }

  onClickThumbnail = id => {
    const updatedList = imagesList.map(image => ({
      ...image,
      isActive: id === image.id,
    }))
    this.setState({imgList: [...updatedList]})
  }

  render() {
    const {imgList} = this.state

    const selectedThumbnailItem = imagesList.find(
      image => image.isActive === true,
    )

    return (
      <div>
        <img
          src={selectedThumbnailItem.imageUrl}
          alt={selectedThumbnailItem.imageAltText}
          className="image"
        />
        <p className="heading">Nature Photography</p>
        <p className="headingWithName">Nature Photography by Rahul</p>
        <ul className="thumbnailImgContainer">
          {imgList.map(image => (
            <ThumbnailItem
              key={image.id}
              imageDetails={image}
              onClickThumbnail={this.onClickThumbnail}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
